public class OutputFormats {

    public static void main(String[] args) {
        
        System.out.print("Ravi");
        System.out.println("Srivastava");
        System.out.printf("%d", 8);
        System.out.printf("%f", 98.73f);
        System.out.printf("%.5f", 9.5468752);
        System.out.printf("%10.2f", 78.924);
        
    }
    
}
